package L_10_Examples;

// Выполнить чтение из одного файла и запись в другой файл с использованием класса PrintWriter.

import java.io.*;

public class L10_10 {
	public static void main(String[] args)throws IOException {
		BufferedReader br = null;
		PrintWriter out=null;
		try {
			// Создание потоков
			br = new BufferedReader(new InputStreamReader(new FileInputStream("MyFile.txt"),"cp1251"));
			out = new PrintWriter("MyFile1.txt","cp1251");
			// Переписывание информации из одного файла в другой
			int lineCount = 0;
			String s;
			while ((s = br.readLine()) != null) {
			lineCount++;
			out.println(lineCount + ": " + s);
			}
		}
		catch (IOException e) {
			 System.out.println("Ошибка !!!!!!!!");
		}
		finally {
			br.close();
			out.flush();
			out.close();
		}
	}
}